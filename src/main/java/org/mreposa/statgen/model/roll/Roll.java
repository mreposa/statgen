package org.mreposa.statgen.model.roll;

import java.util.List;

public class Roll {
    int total;
    List<Integer> values;

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public List<Integer> getValues() {
        return values;
    }

    public void setValues(List<Integer> values) {
        this.values = values;
    }
}
